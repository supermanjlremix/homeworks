user_input = input("Введите число от 0 до 9: ")

if user_input == '0':
    print(")")
elif user_input == '1':
    print("!")
elif user_input == '2':
    print("@")
elif user_input == '3':
    print("#")
elif user_input == '4':
    print("$")
elif user_input == '5':
    print("%")
elif user_input == '6':
    print("^")
elif user_input == '7':
    print("&")
elif user_input == '8':
    print("*")
elif user_input == '9':
    print("(")
else:
    print("Неверное число")
